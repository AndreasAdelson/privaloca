<template>
  <div class="container skb-body">
    <div v-show="loading">
      <div class="loader-container-full">
        <div class="loader" />
      </div>
    </div>
    <a href="/admin/subscription">
      <button
        title="Annulez les modifications"
        type="button"
        class="w-40px p-0 rounded-circle btn-close btn"
      >
        <font-awesome-icon :icon="['fas', 'times']" />
      </button>
    </a>
    <form>
      <div class="">
        <div class="register-card w-100 h-100">
          <!-- First row  -->
          <div class="row">
            <div class="col-6">
              <div class="form-group">
                <fieldset
                  id="name"
                  class="name"
                >
                  <label class="fontUbuntuItalic fontSize14">{{ this.$t('subscription.fields.name') }}</label>
                  <input
                    v-model="formFields.name"
                    v-validate="'required'"
                    type="text"
                    name="name"
                    class="form-control"
                    :placeholder="$t('subscription.placeholder.name')"
                  >
                  <div
                    v-for="errorText in formErrors.name"
                    :key="'name_' + errorText"
                  >
                    <span class="fontUbuntuItalic fontSize13 red-skb">{{ errorText }}</span>
                  </div>
                </fieldset>
              </div>
            </div>

            <div class="col-6">
              <div class="form-group">
                <fieldset
                  id="code"
                  class="code"
                >
                  <label class="fontUbuntuItalic fontSize14">{{ this.$t('subscription.fields.code') }}</label>
                  <input
                    v-model="formFields.code"
                    v-validate="'required'"
                    type="text"
                    name="code"
                    class="form-control"
                    :placeholder="$t('subscription.placeholder.code')"
                  >
                  <div
                    v-for="errorText in formErrors.code"
                    :key="'code_' + errorText"
                  >
                    <span class="fontUbuntuItalic fontSize13 red-skb">{{ errorText }}</span>
                  </div>
                </fieldset>
              </div>
            </div>
          </div>
          <div class="row">
            <div class="col-6">
              <div class="form-group">
                <fieldset
                  id="price"
                  class="price"
                >
                  <label class="fontUbuntuItalic fontSize14">{{ this.$t('subscription.fields.price') }}</label>
                  <input
                    v-model="formFields.price"
                    v-validate="'required'"
                    type="text"
                    name="price"
                    class="form-control"
                    :placeholder="$t('subscription.placeholder.price')"
                  >
                  <div
                    v-for="errorText in formErrors.price"
                    :key="'price_' + errorText"
                  >
                    <span class="fontUbuntuItalic fontSize13 red-skb">{{ errorText }}</span>
                  </div>
                </fieldset>
              </div>
            </div>
            <div class="col-6">
              <div class="form-group">
                <fieldset
                  id="stripeId"
                  class="stripeId"
                >
                  <label class="fontUbuntuItalic fontSize14">{{ this.$t('subscription.fields.stripeId') }}</label>
                  <input
                    v-model="formFields.stripeId"
                    type="text"
                    name="stripeId"
                    class="form-control"
                    :placeholder="$t('subscription.placeholder.stripeId')"
                  >
                  <div
                    v-for="errorText in formErrors.stripeId"
                    :key="'stripeId_' + errorText"
                  >
                    <span class="fontUbuntuItalic fontSize13 red-skb">{{ errorText }}</span>
                  </div>
                </fieldset>
              </div>
            </div>
          </div>
          <div class="row">
            <fieldset
              id="advantages"
              class="col-12 advantages"
            >
              <label class="fontUbuntuItalic fontSize16">{{ $t('subscription.fields.advantages') }}</label>
              <dual-list
                v-if="advantagesAtCreation !== null"
                :items="advantages"
                :item-label-fields="['message']"
                :selected-items-at-creation="advantagesAtCreation"
                @selected-items-change="$onSelectedItemsChange(arguments, 'advantages')"
              />
              <input
                v-model="formFields.advantages"
                v-validate="'required'"
                name="advantages"
                class="d-none"
              >
              <div
                v-for="errorText in formErrors.advantages"
                :key="'advantages_' + errorText"
                class="line-height-1"
              >
                <span class="fontUbuntuItalic fontSize13 red-skb">{{ errorText }}</span>
              </div>
            </fieldset>
          </div>

          <div class="row my-3">
            <div class="col-6 offset-3">
              <button
                type="button"
                class="btn button_skb fontUbuntuItalic"
                @click="$validateForm()"
              >
                {{ this.subscriptionId ? this.$t('commons.edit') : this.$t('commons.create') }}
              </button>
            </div>
          </div>
        </div>
      </div>
    </form>
  </div>
</template>
<script>
  import axios from 'axios';
  import validatorRulesMixin from 'mixins/validatorRulesMixin';
  import DualList from 'components/commons/dual-list';
  import adminFormMixin from 'mixins/adminFormMixin';
  import _ from 'lodash';

  export default {
    components: {
      DualList,
    },
    mixins: [
      validatorRulesMixin,
      adminFormMixin,
    ],
    props: {
      subscriptionId: {
        type: Number,
        default: null,
      },
    },
    data() {
      return {
        loading: true,
        API_URL: '/api/admin/subscriptions' + (this.subscriptionId ? `/${this.subscriptionId}` : ''),
        sousCategorys: [],
        advantagesAtCreation: null,
        advantages: [],
        formFields: {
          name: null,
          price: null,
          code: null,
          advantages: [],
          stripeId: null
        },
        formErrors: {
          name: [],
          price: [],
          code: [],
          advantages: [],
          stripeId: []
        }
      };
    },
    created() {
      let promises = [];
      promises.push(axios.get('/api/admin/advantages'));
      if (this.subscriptionId) {
        promises.push(axios.get(this.API_URL));
      }
      return Promise.all(promises).then(res => {
        this.advantages = _.cloneDeep(res[0].data);
        if (this.subscriptionId) {
          let subscription = _.cloneDeep(res[1].data);
          console.log(subscription);
          this.$removeFieldsNotInForm(subscription, Object.keys(this.formFields));
          this.$setEditForm(subscription);
        }
        this.advantagesAtCreation = _.cloneDeep(this.formFields.advantages);
        this.loading = false;
      }).catch(e => {
        this.$handleError(e);
        this.loading = false;
      });
    },
    methods: {

    },
  };
</script>
